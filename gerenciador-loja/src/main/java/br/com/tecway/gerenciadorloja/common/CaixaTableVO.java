package br.com.tecway.gerenciadorloja.common;

public class CaixaTableVO {

	private String valorData;
	private String valorDinheiro;
	private String valorDebito;
	private String valorCredito;

	public String getValorData() {
		return valorData;
	}

	public void setValorData(String valorData) {
		this.valorData = valorData;
	}

	public String getValorDinheiro() {
		return valorDinheiro;
	}

	public void setValorDinheiro(String valorDinheiro) {
		this.valorDinheiro = valorDinheiro;
	}

	public String getValorDebito() {
		return valorDebito;
	}

	public void setValorDebito(String valorDebito) {
		this.valorDebito = valorDebito;
	}

	public String getValorCredito() {
		return valorCredito;
	}

	public void setValorCredito(String valorCredito) {
		this.valorCredito = valorCredito;
	}

}
